    private void acceptButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_acceptButtonMouseClicked
        String photo = "";
        if (Global.photoChooser.getSelectedFile() == null) 
            JOptionPane.showMessageDialog(this, "No es posible modificar la foto de perfil","Error de cambio", JOptionPane.ERROR_MESSAGE);
        else 
        {
            photo = Global.photoChooser.getSelectedFile().getName();
            updatePersonPhoto(Global.person_id,photo);
            if (Global.update_result == 1) JOptionPane.showMessageDialog(this, "La foto de perfil ha sido actualizada","Actualizaci√≥n exitosa", JOptionPane.INFORMATION_MESSAGE);
            else JOptionPane.showMessageDialog(this, "No es posible modificar la foto de perfil","Error de cambio", JOptionPane.ERROR_MESSAGE);
        }
        Global.photoChooser.setSelectedFile(null);
        editP.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_acceptButtonMouseClicked

